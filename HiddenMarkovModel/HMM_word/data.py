"""
這段程式的功能是定義 HMM 模型的「訓練資料集」。
資料結構為：
- 每個樣本是一個詞（中文字串）及其對應的 BMES 標籤。
- 適用於中文分詞任務，採用隱馬可夫模型（HMM）進行建模。

定義內容包括：
- O：觀察對象集合（所有出現過的字）
- S：隱藏狀態集合（B：詞首，M：詞中，E：詞尾，S：單字詞）
"""

# ============================
# 訓練資料（每筆為詞語: BMES 標註）
# ============================

data = [
    {u"我要吃飯": "SSBE"},      # 「我」與「要」為單字詞，「吃」為詞首，「飯」為詞尾
    {u"天氣不錯": "BEBE"},     # 「天」為詞首，「氣」為詞尾，「不」為詞首，「錯」為詞尾
    {u"謝天謝地": "BMME"}      # 「謝」為詞首，「天」「謝」為詞中，「地」為詞尾
]

# ============================
# O: 觀察符號集合（出現過的所有字）
# ============================

# 訓練資料中出現過的所有字組成觀察集合 O
# 字的集合 = {"我", "要", "吃", "飯", "天", "氣", "不", "錯", "謝", "地"}
# 將作為 HMM 模型中的觀察空間

# ============================
# S: 隱藏狀態集合
# ============================

# 隱藏狀態集合 S = {"B", "M", "E", "S"}
# 分別表示：
# - B: 詞的開頭字（Begin）
# - M: 詞的中間字（Middle）
# - E: 詞的結尾字（End）
# - S: 獨立單字詞（Single）

